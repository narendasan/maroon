section .rodata
gdt64:
    dq 0
.code: equ $ - gdt64                                ;
    dq (1 << 44) | (1 << 47) | (1 << 41) | (1 << 43) | (1 << 53) ;code segment
.data: equ $ - gdt64
    dq (1 << 44) | (1 << 47) | (1 << 41)                         ;data segment
.pointer:
    dw $ - gdt64 - 1
    dq gdt64
